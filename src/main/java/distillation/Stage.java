package distillation;

public class Stage {
    double T;
    double P;
    double L;
    double V;
    double x;
    double y;
    double z;

    public Stage(double T, double P, double L, double V, double x, double y, double z) {
        this.T = T;
        this.P = P;
        this.L = L;
        this.V = V;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}